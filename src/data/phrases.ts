/** Названия фаз игры → контекст для подбора фразы. */
import type { Phase } from '@/types/game';
export function phaseToChatContext(phase: Phase): ChatPhraseContext {
  if (phase === 'BETTING') return 'betting';
  if (phase === 'CARDS') return 'cards';
  if (phase === 'RESULT') return 'result';
  return 'generic';
}
